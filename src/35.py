import numpy as np

def add_numbers(a, b):
    """
    Add two numbers.
    
    Parameters:
        a: First number.
        b: Second number.
        
    Returns:
        The sum of a and b.
    """
    return a + b

def multiply_numbers(a, b):
    """
    Multiply two numbers.
    
    Parameters:
        a: First number.
        b: Second number.
        
    Returns:
        The product of a and b.
    """
    return a * b
